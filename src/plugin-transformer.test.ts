describe("formatAnalysis", () => {
  it("produces readable output with table and summary", () => {
    const plugins = [
      makePlugin(),
      makePlugin({
        manifest: { name: "hook-plugin", version: "1.0.0", description: "Has hooks" },
        hooks: [{ event: "PreToolUse", scriptPath: "check.py" }],
        portability: "low",
        warnings: ["Uses hooks"],
      }),
    ];
    const analysis = analyzeTransform(plugins, "cursor", join(tmpdir(), "nonexistent"));
    const output = formatAnalysis(analysis);

    assert.ok(output.includes("Analysis"));
    assert.ok(output.includes("Cursor"));
    assert.ok(output.includes("test-plugin"));
    assert.ok(output.includes("hook-plugin"));
    assert.ok(output.includes("universal"));
    assert.ok(output.includes("low"));
    assert.ok(output.includes("+ create"));
    assert.ok(output.includes("Summary:"));
    assert.ok(output.includes("Warnings:"));
    assert.ok(output.includes("Uses hooks"));
  });

  it("omits warnings section when none exist", () => {
    const plugins = [makePlugin()];
    const analysis = analyzeTransform(plugins, "cursor", join(tmpdir(), "nonexistent"));
    const output = formatAnalysis(analysis);

    assert.ok(!output.includes("Warnings:"));
  });
});
