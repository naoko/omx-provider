describe("stripClaudeSpecificFrontmatter", () => {
  it("removes allowed-tools, model, color, tools", () => {
    const attrs = {
      description: "My command",
      "allowed-tools": "Bash, Read",
      model: "sonnet",
      color: "green",
      tools: "Glob, Grep",
      "argument-hint": "<name>",
    };
    const stripped = stripClaudeSpecificFrontmatter(attrs);
    assert.equal(stripped.description, "My command");
    assert.equal(stripped["argument-hint"], "<name>");
    assert.ok(!("allowed-tools" in stripped));
    assert.ok(!("model" in stripped));
    assert.ok(!("color" in stripped));
    assert.ok(!("tools" in stripped));
  });
});

