function makePlugin(overrides?: Partial<ParsedPlugin>): ParsedPlugin {
  return {
    manifest: {
      name: "test-plugin",
      version: "1.0.0",
      description: "A test plugin for transformation",
    },
    commands: [
      {
        name: "do-thing",
        description: "Does a thing",
        argumentHint: "<arg>",
        allowedTools: "Bash(pattern:*), Read",
        body: "# Do Thing\n\nStep 1: Do it.\nStep 2: Done.",
      },
    ],
    agents: [
      {
        name: "reviewer",
        description: "Reviews code",
        tools: "Glob, Grep, Read",
        model: "sonnet",
        color: "green",
        body: "# Reviewer\n\nYou review code carefully.",
      },
    ],
    skills: [
      {
        name: "my-skill",
        description: "A skill",
        body: "# My Skill\n\nGuidelines here.",
      },
    ],
    hooks: [],
    portability: "universal",
    warnings: [],
    sourcePath: "/fake/path",
    ...overrides,
  };
}

