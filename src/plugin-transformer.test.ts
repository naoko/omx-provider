describe("analyzeTransform", () => {
  let tempDir: string;

  before(() => {
    tempDir = mkdtempSync(join(tmpdir(), "analyze-"));

    // Pre-create a user-owned file for one plugin
    const rulesDir = join(tempDir, ".cursor", "rules");
    mkdirSync(rulesDir, { recursive: true });
    writeFileSync(
      join(rulesDir, "existing-plugin.md"),
      "# My rules\n\nUser-owned.",
    );

    // Pre-create an omx-generated file for another
    writeFileSync(
      join(rulesDir, "updatable-plugin.md"),
      "# Old\n\n> Auto-generated by omx-provider.\n\nOld content.",
    );
  });

  after(() => {
    rmSync(tempDir, { recursive: true });
  });

  it("detects new plugins as 'create'", () => {
    const plugins = [makePlugin({ manifest: { name: "brand-new", version: "1.0.0", description: "New" } })];
    const analysis = analyzeTransform(plugins, "cursor", tempDir);

    assert.equal(analysis.plugins.length, 1);
    assert.equal(analysis.plugins[0].action, "create");
    assert.equal(analysis.summary.create, 1);
    assert.equal(analysis.summary.update, 0);
    assert.equal(analysis.summary.skip, 0);
  });

  it("detects user-owned files as 'skip'", () => {
    const plugins = [makePlugin({ manifest: { name: "existing-plugin", version: "1.0.0", description: "Existing" } })];
    const analysis = analyzeTransform(plugins, "cursor", tempDir);

    assert.equal(analysis.plugins[0].action, "skip (user-owned)");
    assert.equal(analysis.summary.skip, 1);
  });

  it("detects omx-generated files as 'update'", () => {
    const plugins = [makePlugin({ manifest: { name: "updatable-plugin", version: "1.0.0", description: "Update" } })];
    const analysis = analyzeTransform(plugins, "cursor", tempDir);

    assert.equal(analysis.plugins[0].action, "update");
    assert.equal(analysis.summary.update, 1);
  });

  it("counts components correctly", () => {
    const plugins = [makePlugin()];
    const analysis = analyzeTransform(plugins, "cursor", tempDir);

    assert.equal(analysis.plugins[0].components.commands, 1);
    assert.equal(analysis.plugins[0].components.agents, 1);
    assert.equal(analysis.plugins[0].components.skills, 1);
    assert.equal(analysis.plugins[0].components.hooks, 0);
  });

  it("handles mixed batch correctly", () => {
    const plugins = [
      makePlugin({ manifest: { name: "brand-new", version: "1.0.0", description: "New" } }),
      makePlugin({ manifest: { name: "existing-plugin", version: "1.0.0", description: "Existing" } }),
      makePlugin({ manifest: { name: "updatable-plugin", version: "1.0.0", description: "Update" } }),
    ];
    const analysis = analyzeTransform(plugins, "cursor", tempDir);

    assert.equal(analysis.summary.total, 3);
    assert.equal(analysis.summary.create, 1);
    assert.equal(analysis.summary.skip, 1);
    assert.equal(analysis.summary.update, 1);
  });
});

