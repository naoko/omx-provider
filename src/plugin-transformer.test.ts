describe("transformPlugin marker protection", () => {
  let tempDir: string;

  before(() => {
    tempDir = mkdtempSync(join(tmpdir(), "transform-marker-"));

    // Create a user-owned file that should not be overwritten
    const rulesDir = join(tempDir, ".cursor", "rules");
    mkdirSync(rulesDir, { recursive: true });
    writeFileSync(
      join(rulesDir, "test-plugin.md"),
      "# My custom rules\n\nDo not overwrite.",
    );
  });

  after(() => {
    rmSync(tempDir, { recursive: true });
  });

  it("skips user-owned files", async () => {
    const plugin = makePlugin();
    const result = await transformPlugin(plugin, {
      target: "cursor",
      outputDir: tempDir,
    });

    assert.equal(result.filesWritten.length, 0);
    assert.ok(result.warnings.some((w) => w.includes("user-owned")));

    // Verify original content preserved
    const content = readFileSync(
      join(tempDir, ".cursor", "rules", "test-plugin.md"),
      "utf-8",
    );
    assert.ok(content.includes("My custom rules"));
  });

  it("overwrites omx-generated files", async () => {
    // Write an omx-generated file
    const rulesDir = join(tempDir, ".cursor", "rules");
    writeFileSync(
      join(rulesDir, "overwrite-me.md"),
      "# old\n\n> Auto-generated by omx-provider.\n\nOld content.",
    );

    const plugin = makePlugin({
      manifest: {
        name: "overwrite-me",
        version: "1.0.0",
        description: "Should overwrite",
      },
    });
    const result = await transformPlugin(plugin, {
      target: "cursor",
      outputDir: tempDir,
    });

    assert.equal(result.filesWritten.length, 1);
    const content = readFileSync(result.filesWritten[0], "utf-8");
    assert.ok(content.includes("Should overwrite"));
  });
});

