describe("transformPlugin for cursor", () => {
  let tempDir: string;

  before(() => {
    tempDir = mkdtempSync(join(tmpdir(), "transform-cursor-"));
  });

  after(() => {
    rmSync(tempDir, { recursive: true });
  });

  it("writes cursor rules file", async () => {
    const plugin = makePlugin();
    const result = await transformPlugin(plugin, {
      target: "cursor",
      outputDir: tempDir,
    });

    assert.equal(result.pluginName, "test-plugin");
    assert.equal(result.target, "cursor");
    assert.equal(result.filesWritten.length, 1);
    assert.ok(result.filesWritten[0].endsWith("test-plugin.md"));

    const content = readFileSync(result.filesWritten[0], "utf-8");
    assert.ok(content.includes("# test-plugin"));
    assert.ok(content.includes("Auto-generated by omx-provider"));
  });

  it("respects dry-run mode", async () => {
    const outDir = join(tempDir, "dryrun");
    const plugin = makePlugin();
    const result = await transformPlugin(plugin, {
      target: "cursor",
      outputDir: outDir,
      dryRun: true,
    });

    assert.equal(result.filesWritten.length, 1);
    assert.ok(!existsSync(result.filesWritten[0]));
  });
});

