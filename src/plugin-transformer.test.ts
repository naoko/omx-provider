describe("buildCursorContent", () => {
  it("produces valid cursor rules markdown", () => {
    const plugin = makePlugin();
    const content = buildCursorContent(plugin);

    assert.ok(content.includes("# test-plugin"));
    assert.ok(content.includes("Auto-generated by omx-provider"));
    assert.ok(content.includes("Portability: universal"));
    assert.ok(content.includes("## Commands"));
    assert.ok(content.includes("### /do-thing"));
    assert.ok(content.includes("## Agents"));
    assert.ok(content.includes("### reviewer"));
    assert.ok(content.includes("## Skills"));
    assert.ok(content.includes("### my-skill"));
  });

  it("includes portability notes for hook-based plugins", () => {
    const plugin = makePlugin({
      hooks: [{ event: "PreToolUse", scriptPath: "check.py", description: "Security" }],
      portability: "low",
      warnings: ["Plugin uses hooks"],
    });
    const content = buildCursorContent(plugin);
    assert.ok(content.includes("Portability Notes"));
    assert.ok(content.includes("PreToolUse"));
  });

  it("omits empty sections", () => {
    const plugin = makePlugin({ commands: [], agents: [] });
    const content = buildCursorContent(plugin);
    assert.ok(!content.includes("## Commands"));
    assert.ok(!content.includes("## Agents"));
    assert.ok(content.includes("## Skills"));
  });
});

